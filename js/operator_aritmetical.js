1 + 1 // adition operator
1 - 1 // substractor operator
1 * 1 // multiply operator
1 / 1 // divide operator
1 % 1 // modulus operator
1++   // increment operator
1--   // decrement operator

//////////////////////////////////////////////////////////////

// Postfix Increment/Decrement (x++ / x-- ): 

let a = 5;    // Output: 6 (a is incremented after value b)
let b = a++;  // Output: 5 (original value of a is assigned to b)

//////////////////////////////////////////////////////////////

// Prefix Increment/Decrement (++x / --x): 

let c = 5;    // Output: 6 (c is incremented before to d)
let d = ++c;  // Output: 6 (new value of c is assigned to d)
